package com.jspiders.recall;

public class UsingWhileloop {
public static void main(String[] args) {
	int a=4;
	
	while(a<10){
		System.out.println("a value : "+a);
		a++;
	}
	System.out.println("-----------------------------------");
	
	int  a1=3;
	do{
		System.out.println("a1 value : "+a1);
		a1++;
	}while(a1<10);
	
	
}
}
